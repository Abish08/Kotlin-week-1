package com.example.mc36a.ui.theme

class Comparison Operators {
// Declare variables for comparison operations
    var a: Int = 5
    var b: Int = 5

// Equality check (==)
    println("a == b : ${a == b}")

// Inequality check (!=)
    println("a != b : ${a != b}")

// Less than check (<)
    println("a < b : ${a < b}")

// Greater than check (>)
    println("a > b : ${a > b}")

// Greater than or equal to (>=)
    println("a >= b : ${a >= b}")

// Less than or equal to (<=)
    println("a <= b : ${a <= b}")}