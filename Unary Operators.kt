package com.example.mc36a.ui.theme

class Unary Operators {
// Declare variables for unary operations
    var number: Double = 7.6
    var isCheck: Boolean = true

// Unary plus (+)
    println("+number = ${+number}")

// Unary minus (-)
    println("-number = ${-number}")

// Pre-increment (++number)
    println("++number = ${++number}")

// Pre-decrement (--number)
    println("--number = ${--number}")

// Logical NOT (!)
    println("!isCheck = ${!isCheck}")

// Post-increment (result++)
    println("-----------------------")
    var result: Double = 4.7
    println("result++ : ${result++}") // Note: Post-increment evaluates after use}