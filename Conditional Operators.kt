package com.example.mc36a.ui.theme

class Conditional Operators {
}
// Declare variables for conditional operations
var number1: Int = 5
var number2: Int = 8
var number3: Int = 12
var result: Boolean = false

// Logical AND operator (&&)
result = (number1 > number2) && (number3 > number2)
println("Result of (number1 > number2) && (number3 > number2): $result")

// Logical OR operator (||)
result = (number1 > number2) || (number3 > number2)
println("Result of (number1 > number2) || (number3 > number2): $result")